package ro.planet.documentum.stada.modules.beans;

public class Content {

    private String docId;
    private String docContentType;
    private String objectName;
    private String title;
    private String versionLabel;
    private String dssLanguage;
    private String subject;
    private String docType;
    private String mainFile;
    private String dssFilename;

    public String getDocId() {
        return docId;
    }

    public void setDocId(String docId) {
        this.docId = docId;
    }

    public String getDocContentType() {
        return docContentType;
    }

    public void setDocContentType(String docContentType) {
        this.docContentType = docContentType;
    }

    public String getObjectName() {
        return objectName;
    }

    public void setObjectName(String objectName) {
        this.objectName = objectName;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getVersionLabel() {
        return versionLabel;
    }

    public void setVersionLabel(String versionLabel) {
        this.versionLabel = versionLabel;
    }

    public String getDssLanguage() {
        return dssLanguage;
    }

    public void setDssLanguage(String dssLanguage) {
        this.dssLanguage = dssLanguage;
    }

    public String getSubject() {
        return subject;
    }

    public void setSubject(String subject) {
        this.subject = subject;
    }

    public String getDocType() {
        return docType;
    }

    public void setDocType(String docType) {
        this.docType = docType;
    }

    public String getMainFile() {
        return mainFile;
    }

    public void setMainFile(String mainFile) {
        this.mainFile = mainFile;
    }

    
    public String getDssFilename() {
        return dssFilename;
    }

    
    public void setDssFilename(String dssFilename) {
        this.dssFilename = dssFilename;
    }
    

}
